		if($strNombreCampo=='cantidad') {$this->strMensajecantidad=$strMensajeCampo;}
	}
	
	public function inicializarMensajesDefectoDatosInvalidos() {
		$this->strMensajeid_padre='';
		$this->strMensajeid_hijo='';
		$this->strMensajecantidad='';
	}
	
