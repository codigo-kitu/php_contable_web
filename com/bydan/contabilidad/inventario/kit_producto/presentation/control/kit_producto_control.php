	public function loadIndex() {
		$strTypeOnLoadkit_producto= $this->getDataGeneralString('strTypeOnLoadkit_producto');
		$strTipoPaginaAuxiliarkit_producto= $this->getDataGeneralString('strTipoPaginaAuxiliarkit_producto');
		$strTipoUsuarioAuxiliarkit_producto= $this->getDataGeneralString('strTipoUsuarioAuxiliarkit_producto');
						
		$this->cargarParametrosPagina();
		
		$bitEsPopup=$this->bitEsPopup;
		
		try {
			
			if(Constantes::$BIT_USA_EJB_LOGIC_LAYER) {
				$this->kit_productoLogic->getNewConnexionToDeep();
			}
								
			$this->bitEsMantenimientoRelacionado=$this->bitEsRelacionado;
															
			$this->setCargarParameterDivSecciones(true,true,true,true,true,true,true,false,false,false,false,true);				
			
			$this->index($strTypeOnLoadkit_producto,$strTipoPaginaAuxiliarkit_producto,$strTipoUsuarioAuxiliarkit_producto,'index',$bitEsPopup,false,'','');			
			
			/*MANTENIMIENTO DE SESIONES*/
			if($strTypeOnLoadkit_producto!='onloadhijos') {
				$this->Session->addRemoveSessionsBasesPage('kit_producto','',$this->bitEsRelaciones,$this->bitEsRelacionado);
			}
			/*MANTENIMIENTO DE SESIONES FIN*/
			
			if(Constantes::$BIT_USA_EJB_LOGIC_LAYER) {
				$this->kit_productoLogic->commitNewConnexionToDeep();
				$this->kit_productoLogic->closeNewConnexionToDeep();
			}
		
		} catch(Exception $e) {
			if(Constantes::$BIT_USA_EJB_LOGIC_LAYER) {
				$this->kit_productoLogic->rollbackNewConnexionToDeep();
				$this->kit_productoLogic->closeNewConnexionToDeep();
			}
			
			Funciones::logShowExceptionMessages($e);
			throw $e;
		}
	}
	
