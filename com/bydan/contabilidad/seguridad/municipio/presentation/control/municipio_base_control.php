		if($strNombreCampo=='codigo_departamento') {$this->strMensajecodigo_departamento=$strMensajeCampo;}
		if($strNombreCampo=='codigo_municipio') {$this->strMensajecodigo_municipio=$strMensajeCampo;}
	}
	
