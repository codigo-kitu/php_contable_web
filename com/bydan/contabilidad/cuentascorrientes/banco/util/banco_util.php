	/*LISTA INDICE*/
	public static function getIndiceNuevo(array $bancos,int $iIdNuevobanco) : int {
		$iIndice=0;
		$existe=false;
		
		foreach($bancos as $bancoAux) {
			if($bancoAux->getId()==$iIdNuevobanco) {
				$existe=true;
				break;
			}
				
			$iIndice++;
		}

		if(!$existe) {
			/*SI NO EXISTE TOMA LA ULTIMA FILA*/
			$iIndice=$iIndice-1;
		}
		
		return $iIndice;
	}
	
	public static function getIndiceActual(array $bancos,banco $banco,int $iIndiceActual) : int {
		$iIndice=0;
		$existe=false;
		
		foreach($bancos as $bancoAux) {
			if($bancoAux->getId()==$banco->getId()) {
				$existe=true;
				break;
			}
				
			$iIndice++;
		}		
	
		if(!$existe) {
			/*SI NO EXISTE TOMA LA ULTIMA FILA*/
			$iIndice=$iIndiceActual;
		}
		
		return $iIndice;
	}
	
